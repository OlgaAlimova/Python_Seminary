import string

def count_most_common_words(text):
    # Удаляем знаки препинания и приводим к нижнему регистру
    translator = str.maketrans('', '', string.punctuation)
    text = text.translate(translator).lower()

    # Разбиваем текст на слова
    words = text.split()

    # Создаем словарь для подсчета количества встречаемых слов
    word_counts = {}
    for word in words:
        word_counts[word] = word_counts.get(word, 0) + 1

    # Сортируем словарь по убыванию количества встречаемых слов
    sorted_word_counts = sorted(word_counts.items(), key=lambda x: x[1], reverse=True)

    # Возвращаем 10 самых часто встречающихся слов
    return [word for word, count in sorted_word_counts[:10]]

